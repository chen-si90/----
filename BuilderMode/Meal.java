package BuilderMode;

public class Meal {
    String food;
    String drink;
    public String getFood() {
        return food;
    }
    public String getDrink() {
        return drink;
    }
    public void setFood(String food){
        this.food=food;
    }
    public void setDrink(String drink){
        this.drink=drink;
    }

}

